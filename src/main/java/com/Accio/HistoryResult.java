package com.Accio;

public class HistoryResult {
    String name;
    String link;
    /* getter and setter for name, link */
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }
}
